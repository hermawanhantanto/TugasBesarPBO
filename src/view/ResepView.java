    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        // TODO add your handling code here:
        setComponents(false);
        try{
            Resep r = rControl.searchResep(Integer.parseInt(searchField.getText()));
            if(r == null){
                clearText();
                setUpdateDeleteBtn(false);
                JOptionPane.showConfirmDialog(null, "Data tidak ditemukan", "Konfirmasi", JOptionPane.DEFAULT_OPTION);
            }else {
                int indexPasien = -1;
                int indexDokter = -1;
                int indexObat = -1;
                setUpdateDeleteBtn(true);
                selectedId = Integer.parseInt(searchField.getText());
                int idPasien = r.getPasien().getId_pasien();
                for (Pasien p : pList) {
                    if (p.getId_pasien() == idPasien) {
                        indexPasien = pList.indexOf(p);
                    }
                }
                pasienComboBox.setSelectedIndex(indexPasien);
                String idDokter = r.getDokter().getId_dokter();
                for (Dokter d : dList) {
                    if (d.getId_dokter().equals(idDokter)) {
                        indexDokter = dList.indexOf(d);
                    }
                }
                dokterComboBox.setSelectedIndex(indexDokter);
                int idObat = r.getObat().getId_obat();
                for (Obat o : oList) {
                    if (o.getId_obat() == idObat) {
                        indexObat = oList.indexOf(o);
                    }
                }
                keteranganField.setText(r.getTanggal());
                tanggalField.setText(r.getKeterangan());   
            }
        }catch(Exception e){
            System.out.println("Error: " + e.getMessage());
        }
    }//GEN-LAST:event_searchBtnActionPerformed

