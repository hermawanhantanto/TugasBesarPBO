    private void perawatanTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_perawatanTableMouseClicked
        // TODO add your handling code here
        setUpdateDeleteBtn(true);
        setComponents(false);
        int indexPasien = -1;
        int indexObat = -1;
        
        int clickedRow = perawatanTable.getSelectedRow();
        TableModel tableModel = perawatanTable.getModel();
        
        selectedId = Integer.parseInt(tableModel.getValueAt(clickedRow, 0).toString());
        
        int idPasien = Integer.parseInt(tableModel.getValueAt(clickedRow, 7).toString());
        for (Pasien p : pList) {
            if (p.getId_pasien() == idPasien) {
                indexPasien = pList.indexOf(p);
            }
        }
        pasienComboBox.setSelectedIndex(indexPasien);
        int idObat = Integer.parseInt(tableModel.getValueAt(clickedRow,8).toString());
        for (Obat o : oList) {
            if (o.getId_obat() == idObat) {
                indexObat = oList.indexOf(o);
            }
        }
        
        obatComboBox.setSelectedIndex(indexObat);
        jenisPerawatanField.setText(tableModel.getValueAt(clickedRow, 4).toString());
        durasiField.setText(tableModel.getValueAt(clickedRow, 5).toString());
        statusField.setText(tableModel.getValueAt(clickedRow, 6).toString());
        setUpdateDeleteBtn(true);
        
    }//GEN-LAST:event_perawatanTableMouseClicked

