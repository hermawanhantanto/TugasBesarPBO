    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        // TODO add your handling code here:
        setComponents(false);
        try{
            Perawatan pw = pwControl.searchPerawatan(Integer.parseInt(searchField.getText()));
            if(pw == null){
                clearText();
                setUpdateDeleteBtn(false);
                JOptionPane.showConfirmDialog(null, "Data tidak ditemukan", "Konfirmasi", JOptionPane.DEFAULT_OPTION);
            }else {
                int indexPasien = -1;
                int indexObat = -1;
                setUpdateDeleteBtn(true);
                selectedId = Integer.parseInt(searchField.getText());
                int idPasien = pw.getPasien().getId_pasien();
                for (Pasien p : pList) {
                    if (p.getId_pasien() == idPasien) {
                        indexPasien = pList.indexOf(p);
                    }
                }
                pasienComboBox.setSelectedIndex(indexPasien);
                int idObat = pw.getObat().getId_obat();
                for (Obat o : oList) {
                    if (o.getId_obat() == idObat) {
                        indexObat = oList.indexOf(o);
                    }
                }
                
                jenisPerawatanField.setText(pw.getJenisPerawatan());
                durasiField.setText(Integer.toString(pw.getDurasi()));
                statusField.setText(pw.getStatus());   
            }
        }catch(Exception e){
            System.out.println("Error: " + e.getMessage());
        }
        
    }//GEN-LAST:event_searchBtnActionPerformed

