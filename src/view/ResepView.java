    private void resepTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_resepTableMouseClicked
        // TODO add your handling code here:
        setUpdateDeleteBtn(true);
        setComponents(false);
        int indexPasien = -1;
        int indexDokter = -1;
        int indexObat = -1;
        
        int clickedRow = resepTable.getSelectedRow();
        TableModel tableModel = resepTable.getModel();
        
        selectedId = Integer.parseInt(tableModel.getValueAt(clickedRow, 0).toString());
        
        int idPasien = Integer.parseInt(tableModel.getValueAt(clickedRow, 7).toString());
        for (Pasien p : pList) {
            if (p.getId_pasien() == idPasien) {
                indexPasien = pList.indexOf(p);
            }
        }
        pasienComboBox.setSelectedIndex(indexPasien);
        String idDokter = (tableModel.getValueAt(clickedRow, 8).toString());
        for (Dokter d : dList) {
            if (d.getId_dokter().equals(idDokter)) {
                indexDokter = dList.indexOf(d);
            }
        }
        dokterComboBox.setSelectedIndex(indexDokter);
        int idObat = Integer.parseInt(tableModel.getValueAt(clickedRow,9).toString());
        for (Obat o : oList) {
            if (o.getId_obat() == idObat) {
                indexObat = oList.indexOf(o);
            }
        }
        obatComboBox.setSelectedIndex(indexObat);
        tanggalField.setText(tableModel.getValueAt(clickedRow, 5).toString());
        keteranganField.setText(tableModel.getValueAt(clickedRow, 6).toString());
        setUpdateDeleteBtn(true);
    }//GEN-LAST:event_resepTableMouseClicked

